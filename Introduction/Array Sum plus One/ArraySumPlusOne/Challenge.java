public class Challenge {	 	         	 	 	      	       	         	
	 	         	 	 	      	       	         	
    public static int sumPlusOne(int[] numbers) {	 	         	 	 	      	       	         	
	 	         	 	 	      	       	         	
        int sum = 0;	 	         	 	 	      	       	         	
	 	         	 	 	      	       	         	
        for (int i = 0; i < numbers.length; i++) {	 	         	 	 	      	       	         	
	 	         	 	 	      	       	         	
            sum += numbers[i] + 1;	 	         	 	 	      	       	         	
	 	         	 	 	      	       	         	
        }	 	         	 	 	      	       	         	
	 	         	 	 	      	       	         	
        return sum;	 	         	 	 	      	       	         	
	 	         	 	 	      	       	         	
    }	 	         	 	 	      	       	         	
	 	         	 	 	      	       	         	
}