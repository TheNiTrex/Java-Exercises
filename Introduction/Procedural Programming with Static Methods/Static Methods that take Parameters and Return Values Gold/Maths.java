public class Maths	 	         	 	 	      	       	         	
{	 	         	 	 	      	       	         	
     public static int abs(int x)	 	         	 	 	      	       	         	
         {	 	         	 	 	      	       	         	
           if (x>0) return x;	 	         	 	 	      	       	         	
           return -x;	 	         	 	 	      	       	         	
         }	 	         	 	 	      	       	         	
	 	         	 	 	      	       	         	
     public static int max(int x, int y)	 	         	 	 	      	       	         	
         {	 	         	 	 	      	       	         	
           int max = x > y ? x : y;	 	         	 	 	      	       	         	
           return max;	 	         	 	 	      	       	         	
         }	 	         	 	 	      	       	         	
}